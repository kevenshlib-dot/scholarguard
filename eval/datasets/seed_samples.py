def _fill_template(
    template: str,
    discipline: str,
    rng: random.Random,
) -> str:
    """填充模板变量，确保250-500字符"""
    topic = rng.choice(_HUMAN_TOPICS[discipline])
    filler = rng.choice(_HUMAN_FILLERS)
    text = template.format(topic=topic, filler=filler)

    # 若文本偏短，追加补充句
    short_extras = [
        "这一问题值得后续研究继续深入探索。",
        "当然，本文的分析也存在一定局限。",
        "进一步的讨论将在下一节展开。",
        "限于篇幅，此处暂不赘述。",
        "后续研究可以在此基础上拓展分析范围。",
    ]
    while len(text) < 250:
        text += rng.choice(short_extras)

    # 若文本偏长，截断到合理长度（在句号处截断）
    if len(text) > 500:
        cut = text[:500]
        last_period = cut.rfind("。")
        if last_period > 250:
            text = cut[: last_period + 1]
        else:
            text = cut

    return text


