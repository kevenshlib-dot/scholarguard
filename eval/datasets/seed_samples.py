def generate_all_samples(seed: int = 42) -> list[SeedSample]:
    """
    生成全部550条种子评测样本。

    Args:
        seed: 随机种子，确保可复现

    Returns:
        包含550条SeedSample的列表
    """
    rng = random.Random(seed)

    samples = []
    samples.extend(_generate_human_samples(rng))
    samples.extend(_generate_ai_samples(rng))
    samples.extend(_generate_edited_samples(rng))
    samples.extend(_generate_humanizer_samples(rng))

    # 验证
    assert len(samples) == 550, f"Expected 550 samples, got {len(samples)}"

    # 验证所有样本 >= 200 字符
    for i, s in enumerate(samples):
        assert len(s.text_content) >= 200, (
            f"Sample {i} too short: {len(s.text_content)} chars"
        )

    # 验证哈希唯一性（模板+不同主题/filler组合应产生不同文本）
    hashes = {s.text_hash for s in samples}
    if len(hashes) < len(samples):
        # 有重复时追加序号后缀使其唯一
        seen = set()
        for s in samples:
            if s.text_hash in seen:
                s.text_content += f"（样本编号：{id(s) % 10000}）"
                s.text_hash = hashlib.sha256(
                    s.text_content.encode("utf-8")
                ).hexdigest()
            seen.add(s.text_hash)

    return samples


